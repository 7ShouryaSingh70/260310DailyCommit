User = input('Enter User Input:\n')

if User == 'LOL':
    print('LOL = laughing out loud')
elif User == 'ROFL':
    print('ROFL = rolling on the floor laughing')
elif User == 'LMK':
    print('LMK = Let Me Know')
elif User == 'SMH':
    print('SMH = Shaking my head')
else:
    print("Sorry, don't know that one")